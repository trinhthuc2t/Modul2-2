package Bai_3_NumberFormatException;

public class CalculationExample {
    public void calc(int x, int y) {
        int a = x + y;
        int b = x - y;
        int c = x * y;
        int d = x / y;
        System.out.println("x + y = " + a);
        System.out.println("x - y = " + b);
        System.out.println("x * y = " + c);
        System.out.println("x : y = " + d);
    }
}
